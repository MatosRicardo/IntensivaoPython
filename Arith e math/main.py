## Aritmédica e Math

x = 3.14
y = -4
z = 5

result = round(x) ##Arredonda o valor para o inteiro mais próximo
print(result)

result = abs(y) ##Retorna o valor absoluto
print(result)

result = pow(z, 2) ## Retorna o valor de z elevado a 2
print(result)

Maximo = max(x, y, z) ## Retorna o valor máximo, é mais usado

Minimo = min(x, y, z) ## Retorna o valor mínimo, é mais usado